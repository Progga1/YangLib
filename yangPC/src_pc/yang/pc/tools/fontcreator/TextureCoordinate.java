package yang.pc.tools.fontcreator;

public class TextureCoordinate {

	public float x1;
	public float y1;
	public float x2;
	public float y2;
	public float tw;
	public float th;

	public TextureCoordinate(float x1, float y1, float x2, float y2, float tw, float th) {
		this.x1 = x1;
		this.y1 = y1;
		this.x2 = x2;
		this.y2 = y2;
		this.tw = tw;
		this.th = th;
	}
	
}
